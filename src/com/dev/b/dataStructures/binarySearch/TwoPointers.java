package com.dev.b.dataStructures.binarySearch;

public class TwoPointers {

    public int function(int[] arr){
        int left = 0;
        int right = arr.length-1;
        int ans = 0;
        boolean condition = false;
        while(left<right){
            //Code logic
            if(condition){
                left++;
            }
            else{
                right--;
            }
        }
        return ans;
    }
}
